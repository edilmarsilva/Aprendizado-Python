"""Crie um programa que leia dois números e mostre a soma entre eles."""
n1 = int(input('\033[35mDIGITE UM NÚMERO: '))
n2 = int(input('\033[35mDIGITE OUTRO NÚMERO: '))
s = n1 + n2
print('\033[1;34m-=' * 12)
print(f'A soma de \033[32m{n1}\033[m + \033[32m{n2} = \033[33m{s} !!!')
print('\033[1;34m-=' * 12)
