"""Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e o seu antecessor."""
print('\033[36m===============================***ÍNICIO***===============================')
n1 = int(input('\033[1;32mDigite um número:\033[m '))
ant = n1 - 1
sus = n1 + 1
print(f'O antecessor de \033[4m{n1}\033[m é \033[31m{ant}\033[m e seu sucessor é \033[32m{sus}\033[m')
print('\033[36m=================================***FIM***=================================')
