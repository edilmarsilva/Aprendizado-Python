'''
Escreva um programa que leia três notas de um aluno digitados pelo usuário e
retorne a média dele
'''

n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
n3 = float(input('Digite a tercera nota: '))
media = (n1 + n2 + n3) / 3
print(f'A média das NOTAS é {media}')
