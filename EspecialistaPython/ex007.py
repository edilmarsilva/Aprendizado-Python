'''
Escreva um programa que leia um número e diga qual o seu dobro, triplo, quadrado, cubo e raiz quadrada
'''

num = int(input('Digite um número: '))
print(f'O dobro é {2 * num}')
print(f'O triplo é {3 * num}')
print(f'O quadrado é {num ** 2}')
print(f'O cubo é {num ** 3}')
print(f'A raíz quadrada é {num ** (1/2)}')
